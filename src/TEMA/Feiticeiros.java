package TEMA;

public class Feiticeiros {
    string nome;
    double vida;
    double ataque;
    double defesa;
    string Arma;
    double mana;

    public Feiticeiros( string nome, double vida, double ataque, double defesa, string arma, double mana) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.Arma = arma;
        this.mana = mana;
    }

}
