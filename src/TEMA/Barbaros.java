package TEMA;

public class Barbaros {
    string nome;
    double vida;
    double ataque;
    double defesa;
    string Arma;

    public Barbaros( string nome, double vida, double ataque, double defesa, string arma) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.Arma = arma;
    }
}
