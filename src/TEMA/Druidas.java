package TEMA;

public class Druidas {
    string nome;
    double vida;
    double ataque;
    double defesa;
    string Arma;
    double fe;

    public Druidas( string nome, double vida, double ataque, double defesa, string arma, double fe) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.Arma = arma;
        this.fe = fe;
    }
}
